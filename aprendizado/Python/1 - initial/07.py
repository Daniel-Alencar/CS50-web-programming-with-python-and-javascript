# create an empty 'set'
s = set()

# add elements to the 'set'
s.add(5)
s.add(34)
s.add(-14)
s.add(5)

# remove elements to the 'set'
s.remove(-14)

print(s)
print(f"The set has {len(s)} elements")

# É um tipo de dado que guarda dados que não se repetem dentro deste conjunto (mesmo que você tente adicionar um item que tenha o mesmo valor de um item dentro da coleção)
